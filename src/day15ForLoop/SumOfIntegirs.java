package day15ForLoop;

public class SumOfIntegirs {

    public static void main(String[] args) {
// 3. Write a program that can calculate the sum of all integers between 1 to 100
        
int sum=0;
        for (int i = 1; i <101 ; i++) {
            sum+=i;
            System.out.println("sum = " + sum);
            
        }





    }
}
