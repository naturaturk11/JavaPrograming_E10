package day6_PrimitiveLifeCasting;

public class Birthday {


    public static void main(String[] args) {

        String name="Hasan";
        int birthDay= 03;
        int birthMonth= 5;
        int birthYear= 1981;
        String birthOfPlace= "Alanya";

            // even single code is used with backward slash as seen \'
        System.out.println(name + " was born" + " in " + birthOfPlace+ " on "+ birthMonth+"/"+birthDay+ "/"+ birthYear+ ".");

        String bookName= "GameOfThorenes";

        System.out.println("MY favorite book name is" + " \""+ bookName+ "\""+ ".");



    }
}
